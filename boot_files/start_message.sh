#!/bin/bash
# Colors for better readability

GREEN='\033[0;32m'
CYAN='\033[0;36m'
NC='\033[0m' # No Color
echo -e "${CYAN}"

cat << "EOF"
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
 _     _  _______  ___      _______  _______  __   __  _______          ______   ______    _______  ______   
| | _ | ||       ||   |    |       ||       ||  |_|  ||       |        |      | |    _ |  |       ||    _ |  
| || || ||    ___||   |    |       ||   _   ||       ||    ___|        |  _    ||   | ||  |   _   ||   | ||  
|       ||   |___ |   |    |       ||  | |  ||       ||   |___         | | |   ||   |_||_ |  | |  ||   |_||_ 
|       ||    ___||   |___ |      _||  |_|  ||       ||    ___|        | |_|   ||    __  ||  |_|  ||    __  |
|   _   ||   |___ |       ||     |_ |       || ||_|| ||   |___         |       ||   |  | ||       ||   |  | |
|__| |__||_______||_______||_______||_______||_|   |_||_______|        |______| |___|  |_||_______||___|  |_|
~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
EOF
echo -e "${NC}"

echo "================="
echo -e "${GREEN}CONNECTION STATUS${NC}"
echo "================="
nmcli connection show --active
ip -brief -4 address show
echo ""

echo "=============="
echo -e "${GREEN}SSH DETAILS${NC}"
echo "=============="
echo "Hostname: $(hostname)"
echo "User:     $USER"
echo ""

if command -v pihole &> /dev/null; then
    echo "=============="
    echo -e "${GREEN}PI HOLE STATUS${NC}"
    echo "=============="
    pihole status
    echo ""
fi